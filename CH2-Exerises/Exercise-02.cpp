#include <iostream>
#include <iomanip> 
using namespace std;
int main()
{
    // use setw() function to set the width of the output field
    cout << "1990" << setw(8) << "135" << endl;
    cout << "1991" << setw(8) << "7290" << endl;
    cout << "1992" << setw(8) << "11300" << endl;
    cout << "1993" << setw(8) << "16200" << endl;
    return 0;
}
